// レイヤーNo表示
static void print_layer_status(void) {
    oled_set_cursor(0, 10);
    oled_write_P(PSTR("Layer"), false);
    oled_set_cursor(0, 11);
    switch (get_highest_layer(layer_state)) {
        case 1:  oled_write_raw_P(img_num1, sizeof(img_num1)); break;
        case 2:  oled_write_raw_P(img_num2, sizeof(img_num2)); break;
        case 3:  oled_write_raw_P(img_num3, sizeof(img_num3)); break;
        case 4:  oled_write_raw_P(img_num4, sizeof(img_num4)); break;
        default: oled_write_raw_P(img_num0, sizeof(img_num0)); break;
    }
}
// OLEDメイン処理(メイン側)
void keyball_oled_render_mymain(void) {

    oled_write_P(PSTR("CPI") , false);
    oled_set_cursor(0, 1);
    oled_write(format_4d(keyball_get_cpi()) + 1, false);
    oled_write_P(PSTR("00 "), false);

    oled_set_cursor(0, 2);
    oled_write_P(PSTR("DIV"), false);
    oled_write_P(PSTR("      "), false);
    oled_write_char('0' + keyball_get_scroll_div(), false);

    oled_set_cursor(0, 4);
    oled_write_P(PSTR("SCR"), false);
    // indicate scroll mode: on/off
    if (keyball.scroll_mode) {
        oled_write_P(LFSTR_ON, false);
    } else {
        oled_write_P(LFSTR_OFF, false);
    }
    oled_set_cursor(0, 5);
    oled_write_P(PSTR("   "), false);
    // indicate scroll snap mode: "VT" (vertical), "HO" (horizontal), and "SCR" (free)
#if 1 && KEYBALL_SCROLLSNAP_ENABLE == 2
    switch (keyball_get_scrollsnap_mode()) {
        case KEYBALL_SCROLLSNAP_MODE_VERTICAL:
            oled_write_P(PSTR("VT"), false);
            break;
        case KEYBALL_SCROLLSNAP_MODE_HORIZONTAL:
            oled_write_P(PSTR("HO"), false);
            break;
        default:
            oled_write_P(PSTR("FR"), false);
            break;
    }
#endif
    //oled_write_P(PSTR("\xB1"), false);

#ifdef POINTING_DEVICE_AUTO_MOUSE_ENABLE
    oled_set_cursor(0, 6);
    oled_write_P(PSTR("AMR"), false);
    if (get_auto_mouse_enable()) {
        oled_write_P(LFSTR_ON, false);
    } else {
        oled_write_P(LFSTR_OFF, false);
    }
    oled_set_cursor(0,7);
    oled_write_P(PSTR(" "), false);
    oled_write(format_4d(get_auto_mouse_timeout() / 10) + 1, false);
    oled_write_char('0', false);
#endif

    print_layer_status();
}

